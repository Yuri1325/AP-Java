class Example1 {
    public static void main(String[] args) {
        int[] nums = { -2, 0, 5, 7, 9 };
        // Iterate the array w/ a for loop
        for (int i = 0; i < nums.length; i++)
            System.out.println(nums[i]);
        // Iterate the array w/ a for-each loop
        for (int n : nums)
            System.out.println(n);
    }

}